CREATE TABLE prediction_entity
(
    id         BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    user_id    BIGINT,
    match_id   BIGINT,
    home_team  VARCHAR(255),
    away_team  VARCHAR(255),
    home_score INTEGER,
    away_score INTEGER,
    correct    BOOLEAN,
    points     INTEGER,
    date       TIMESTAMP WITHOUT TIME ZONE,
    gameweek   INTEGER,
    CONSTRAINT pk_prediction_entity PRIMARY KEY (id)
);

